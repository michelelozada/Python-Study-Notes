# 1 - Igualdade (==)
print(4 == 4.0)  # Retorna: True


# 2 - Diferente de (!=)
print(4 != 4.0)  # False


# 3 - Maior que (>)
print(5 > 3) # True


# 4 - Menor que (<)
print(2.5 < 10) # True


# 5 - Maior ou igual a (>=)
print(5 >= 13) # False
print(5.0 >= 5) # True


# 6 - Menor ou igual a (<=)
print(10 <= 5) # False
print(10 <= 10.0) # True


# 7 - Comparando strings
print('bom' < 'dia') # True
print('bom' == 'dia') # False
print('bom' > 'dia') # False
print('dia' == 'dia') # True